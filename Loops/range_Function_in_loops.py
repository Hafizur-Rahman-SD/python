# Main consept of this code is to use the range function in a for loop

'''
range (start,stop,step_size)
start: The starting number of the range (inclusive).
stop: The ending number of the range (exclusive).
step_size: The increment between each number in the range (optional, default is 1). its not necessary to use it)
'''


