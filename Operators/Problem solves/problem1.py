# Q1. Code to implement basic arithmetic operations on integers number take from user.

# Take two numbers as input from the user
x = int(input("Enter the first number (x): "))
y = int(input("Enter the second number (y): "))

print("\n--- Arithmetic Operations ---")
print("Summasion of x + y =", x + y)      # Addition
print("Subtraction of x - y =", x - y)      # Subtraction
print("Multiplication of x * y =", x * y)      # Multiplication
print("Division (float) of x / y =", x / y)      # Division (float)
print("Floor division of x // y =", x // y)    # Floor division
print("Modulo of x % y =", x % y)      # Modulo
print("Exponentiation of x ** y =", x ** y)    # Exponentiation

print("\n--- Comparison Operations ---")
print("x > y:", x > y)       # Greater than
print("x < y:", x < y)       # Less than
print("x == y:", x == y)     # Equal to
print("x != y:", x != y)     # Not equal to
print("x >= y:", x >= y)     # Greater than or equal to
print("x <= y:", x <= y)     # Less than or equal to
