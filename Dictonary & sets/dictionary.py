
# Dictionary in Python
# A dictionary is a collection which is unordered, changeable and indexed. No duplicate members.

# Dictionaries are written with curly brackets, and have keys and values.
marks ={
"Hafizur": 100,
"Rahman": 90,
"Shakib": 95,
"Riyad": 85,
"Asma": 80,
}

print(marks,type(marks))
# Dictionary is unordered, changeable and indexed

print(marks["Hafizur"]) # just give us Hafizurs mark in outout

# proparty of python dictionary
# 1. unordered  
# 2. its mutable 
# 3. indexed
# 4. no duplicate members
# 5. keys are unique
# 6. values can be any data type
# 7. keys can be any immutable data type
# 8. values can be any data type
# 9. keys are case sensitive    
