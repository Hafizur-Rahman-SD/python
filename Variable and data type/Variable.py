# Rules of variables in python:

# 1. A variable name must start with a letter or an underscore (_), followed by letters, digits, or underscores.
# 2. Variable names are case-sensitive (e.g., `myVar` and `myvar` are different variables).
# 3. Variable names cannot be a reserved keyword in Python (e.g., `if`, `else`, `while`, etc.).
# 4. Variable names should be descriptive and meaningful to improve code readability.
# 5. Variable names should not contain spaces or special characters (except for underscores).
# 6. Variable names should not start with a digit.
# 7. Variable names should be kept short but descriptive enough to convey their purpose.
